public class Print_vs_Println {
    
    public static void main(String[] args) {
        
        // println() prints text and moves to a new line
        // Output:
        // a
        // b
        // c
        System.out.println("a");
        System.out.println("b");
        System.out.println("c");

        // print() prints text but it does not move to a new line
        // Output: a b c
        System.out.print("a");
        System.out.print("b");
        System.out.print("c");

    }

}
