/**
 * Workbook 5.4 creates a for loop that steps down.
 * 99 bottles of beer on the wall, 99 bottles of beer!
 * take on down, pass it around, 98 bottles of beer on the wall!
 * etc.
 * 
 * Task 1 - Write a function
 * Function name -- sing. <------
 * @param i (int)
 *
 *
 * Inside the function:
 *  1. print:
 *    <i> bottles of beer on the wall, <i> bottles of beer!
 *    take one down, pass it around <i-1>, bottles of beer on the wall!
 * 
 * 2. Parameters:
 * Function name -- sing.
 * @param i (int)  <-------
 * 
 * 3. Return value: void
 * 
 * 4. Inside the function, add the following logic:
 * Inside the function: <--------
 * 1. print:
 *     <i> bottles of beer on the wall, <i> bottles of beer!
 *     take one down, pass it around <i-1>, bottles of beer on the wall!
 * 
 * Task 2 - Write a loop
   Make a for loop that:
   starts from i = 99.
   finishes at i = 1.
   loops down in steps of 1.
   Call the function during each run. Your final output should start from 99 bottles of beer on the wall.
 * 
 * 
 */

public class Workbook_5_4 {

    public static void main(String[] args) {
        for (int i = 99; i > 0; i--) {
            sing(i);
        }
    }

    public static void sing(int i) {
            System.out.println(i + " bottles of beer on the wall, " + i + " bottles of beer!");
            System.out.println("Take one down, pass it around, " + (i-1) + " bottles of beer on the wall!");
    }
    
}
